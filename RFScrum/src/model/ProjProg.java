package model;

public class ProjProg {

	private int projeto;
	private int programador;
	private boolean scrumMaster;
	public int getProjeto() {
		return projeto;
	}
	public void setProjeto(int projeto) {
		this.projeto = projeto;
	}
	public int getProgramador() {
		return programador;
	}
	public void setProgramador(int programador) {
		this.programador = programador;
	}
	public boolean isScrumMaster() {
		return scrumMaster;
	}
	public void setScrumMaster(boolean scrumMaster) {
		this.scrumMaster = scrumMaster;
	}
	
	
	
}
